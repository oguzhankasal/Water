    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = txtUserName.getText().trim();
        char[] arr = txtpass.getPassword();
        String userpass = String.valueOf(arr).trim();
        
        try {
            String query = " SELECT * FROM `login` WHERE `Kullanıcı Adı` = ? AND `Sifre` = ? ";
            PreparedStatement pre = db.preBaglan(query);
            pre.setString(1, username);
            pre.setString(2, userpass);
            ResultSet rs = pre.executeQuery();
            if(rs.next()) {
                // kullanıcı girişi başarılı
                DashBoard dh = new DashBoard();
                dh.setVisible(true);
                dispose();
            }else {
                // kullanıcı adı yada şifre hatalı
                JOptionPane.showMessageDialog(this, "Kullanıcı bilgileri hatalı!");
            }
        } catch (Exception e) {
            System.err.println("Login Error : " + e);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

